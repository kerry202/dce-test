package cn.dagongniu.oax.https;

import java.io.Serializable;

/**
 * @describe 请求头部bean
 */

public class HttpBaseBean<T> {

/*  {
        "success":true,
        "msg":"注册成功！ ",
        "data":null
    }*/

    private int code;
    private boolean success;
    private String msg;
    private T data;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    @Override
    public String toString() {
        return "HttpBaseBean{" +
                "code=" + code +
                ", success=" + success +
                ", msg='" + msg + '\'' +
                ", data=" + data +
                '}';
    }
}
